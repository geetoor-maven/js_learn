
// arrow function dengan parameter
const sayName = (name) => {
    console.log(`Hai ${name}`)
} 

sayName("agus")

// arrow function tanpa parameter
const sayHello = () => {
    console.log("Selamat pagi semuanya!")
};

sayHello();

// arrow function yang satu baris
const multiply = (a, b) => a * b;
console.log(multiply(3, 4));


const sasy = () => {
    console.log("ahh")
}

sasy()